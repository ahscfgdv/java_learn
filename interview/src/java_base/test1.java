package java_base;

public class test1 {

    public static void main(String[] args) {
        int i = 1;

        System.out.println("i = " + i);
        System.out.println("i = " + ++i);
        System.out.println("i = " + i++);
        System.out.println("i = " + i);
        System.out.println("i = " + --i);
        System.out.println("i = " + i--);
        System.out.println("i = " + i);
    }
}
